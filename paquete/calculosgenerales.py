def sumar(op1,op2):
    print("suma : " ,op1+op2)

def resta(op1,op2):
    print("resta: " ,op1-op2)

def multiplicar(op1,op2):
    print("multiplicar : " ,op1*op2)

def division(dividendo,divisor):
    print("div : " ,dividendo/divisor)

def potencia(base,exponente):
    print("potencia : " ,base**exponente)

def redondear(num):
    print("redondeo : " ,round(num))